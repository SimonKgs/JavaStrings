public class SpecialCharacters {
    public static void main(String[] args) {
        // Special characters
        // new line \n
        String string1 = "Hello \nworld";
        System.out.println("string1 = " + string1);

        // tab \t
        System.out.println("string1 \t\t=\n " + string1);

        // escape char \
        System.out.println("string1 \"= " + string1);

        // \\
        System.out.println("STRING WIHT A \\ Character");
    }
}
